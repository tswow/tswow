 export interface ClientPatch {
    address: number
    values: number[]
}

export interface ClientPatchCat {
    name: string
    patches: ClientPatch[]
}

export function patch(name: string, patches: [number,number[]][]): ClientPatchCat
{
    return {name,patches:patches.map(x=>({address:x[0],values:x[1]}))}
}

